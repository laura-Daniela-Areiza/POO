package Entidad;

import java.util.Date;

/**
 *
 * @author dani_
 */
public class Persona {
    
    private String name;
    private Date DOB;

    public Persona() {
    }

    public Persona(String name, Date DOB) {
        this.name = name;
        this.DOB = DOB;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDOB() {
        return DOB;
    }

    public void setDOB(Date DOB) {
        this.DOB = DOB;
    }

    @Override
    public String toString() {
        return "Persona{" + name + " naci√≥ " + DOB + '}';
    }
    
    
}
